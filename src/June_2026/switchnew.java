package June_2026;

public class switchnew {
    public static void main(String[] args) {
        String fruit= "Mango";
        switch(fruit){
            case "Apple":
                System.out.println("Apple");
            case "Mango":
                System.out.println("Mango");
                case "Banana":
                System.out.println("Banana");
                break;
        }
    }
}
