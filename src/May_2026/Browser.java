package May_2026;

public class Browser {
    public static void main(String[] args) {
        String browser = "Firefox";
        switch (browser)
        {
            case "Edge":
                System.out.println("Browser is Edge");
                break;
            case "Chrome":
                System.out.println("Browser is Chrome");
                break;
            case "Firefox":
                System.out.println("Browser is Firefox");
                break;
        }

    }


}
